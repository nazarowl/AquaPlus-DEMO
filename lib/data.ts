export function getModelImage(name: string, size?: string): string | null {
  if (size) {
    const sizeKey = size.replace(/[^0-9x]/g, '').replace('x', '');
    const combo = name + '_' + sizeKey;
    if (MODEL_IMAGES[combo]) return MODEL_IMAGES[combo];
  }
  return MODEL_IMAGES[name] || null;
}

export function getCollectionImages(name: string): string[] {
  return COLLECTION_IMAGES[name] || [];
}

